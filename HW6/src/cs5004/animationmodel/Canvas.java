package cs5004.animationmodel;

public class Canvas {
  int width;
  int height;

  public Canvas() {
    this.width = 0;
    this.height = 0;
  }

  public int getWidth() {
    return width;
  }

  public void setWidth(int width) {
    this.width = width;
  }

  public int getHeight() {
    return height;
  }

  public void setHeight(int height) {
    this.height = height;
  }
}
